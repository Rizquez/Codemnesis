from src.tools.docstring import format_docstring
from common.constants import ALGORITHM_VERSION, NO_METHOD, NO_FUNCTION, NO_CLASS, NO_MODULE, NO_ATTRIBUTE

if TYPE_CHECKING:
    from src.models import ModuleInfo
